void generateInput(vector<vector<string>>& grid, int h, int w) {
    grid = vector<vector<string>>(h + 1, vector<string>(w + 1, ""));
    for (int i = 1; i <= h; ++i) {
        for (int j = 1; j <= w; ++j) {
            grid[i][j] = "snuke";
        }
    }
}

